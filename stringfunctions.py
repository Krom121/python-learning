# String Functions

myStr = 'hello world'

# Capitalize

print(myStr.capitalize())

# swap case 

print(myStr.swapcase())

# get the length 

print(len(myStr))

# replace 

print(myStr.replace('world', 'everyone'))

sub = 'l'
print(myStr.count(sub))

# starts with
print(myStr.startswith('hello'))

#endswith
print(myStr.endswith('world'))

# split list

print(myStr.split())

# find 

print(myStr.find('r'))

# index 

print(myStr.index('o'))

